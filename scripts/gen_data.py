def main():
    for task in experts:
        env = ALL_V2_ENVIRONMENTS_GOAL_OBSERVABLE[f'{task}-goal-observable']()
        expert = experts[task]()
        #obs, info = env.reset()
        obs = env.reset()
        done = False
        n_goal_achieved_total = 0
        total_reward = 0
        step_count = 0

        while (not done):
            action = expert.get_action(obs)
                
            obs, reward, terminated, env_info = env.step(action)
            done = terminated
            n_goal_achieved_total += env_info['success']
            total_reward += reward
            step_count += 1
            print(f"Step: {step_count}, Reward: {reward}, Goal Achieved: {env_info['success']}")

        if n_goal_achieved_total > 10:
            color = 'green'
        else: 
            color = 'red'
        cprint(f'Task: {task}, Reward: {total_reward}, Goal Achieved: {n_goal_achieved_total}, Lenght: {step_count}', color)





if __name__ == '__main__':
    main()